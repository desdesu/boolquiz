    Runnable gimsistem = new Runnable() {
        @Override
        public void run() {
            SystemGame x = new SystemGame(true, "");
            try {
                while (loop == 1) {
                    Thread.sleep(1000);
                    if (detik % 5 == 0) {
                        soal.setText(x.getSoal());
                    }
                    if (detik >= 0) {
                        waktu.setText(Integer.toString(detik));
                    }
                    if (jawab == 1) {
                        x.setScore();
                    }
                    if (SystemGame.bs == true) {
                        jawab = 0;
                        loop = 0;
                        jawabanBenar();
                        SystemGame.bs = false;
                        Thread.sleep(3000);
                        jbenar.setVisible(false);
                        score.setText(Integer.toString(x.getScore()));
                    }
                    if (SystemGame.bs == false && jawab == 1 || detik==0) {
                        jsalah.setSize(400, 300);
                        jsalah.setLocationRelativeTo(rootPane);
                        jsalah.setVisible(true);
                        Thread.sleep(3000);
                        jsalah.setVisible(false);
                        jawabanSalah();
                        break;
                    }
                    detik--;
                }

            } catch (Exception e) {
            }
        }
    };

