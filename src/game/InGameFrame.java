    @Override
    public void jawabanSalah() {
        SystemGame x = new SystemGame(true, "");
        try {
                String sql = "Select * from player ORDER BY id_player ASC";
                java.sql.Connection conn = (Connection) Config.configDB();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                while (res.next()) {
                    id = Integer.parseInt(res.getString("id_player"));
                }
            } catch (Exception e) {
            }
        try {
            String sql = "Select * from player where id_player='" + id + "'";
            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                info = "Nama : "+res.getString(2)+" Score : "+Integer.parseInt(res.getString("score"));
            }
        } catch (Exception e) {
        }
        namascore.setText(info);
        loop=0;
        gameover.setVisible(true);
        gameover.setSize(400, 300);
        gameover.setLocationRelativeTo(this);
    }
